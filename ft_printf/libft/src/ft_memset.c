#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*now;
	int		i;

	if (len == 0)
		return (b);
	now = (char *)b;
	i = -1;
	while (++i < (int)len)
		now[i] = c;
	return (b);
}
