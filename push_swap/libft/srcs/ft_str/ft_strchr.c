char	*ft_strchr(const char *s, int c)
{
	int i;
	int len;

	i = 0;
	len = 0;
	while (s[len])
		len++;
	while (i < len && s[i] != (char)c)
		i++;
	if (s[i] == (char)c)
		return ((char *)&s[i]);
	return (0);
}
