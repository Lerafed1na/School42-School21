#include "libft.h"

void	ft_putnbr(int c)
{
	if (c == -2147483648)
		ft_putstr("-2147483648");
	else if (c < 0)
	{
		ft_putchar('-');
		ft_putnbr(-c);
	}
	else if (c > 9)
	{
		ft_putnbr(c / 10);
		ft_putchar(c % 10 + '0');
	}
	else
		ft_putchar(c + '0');
}
